# Define two integers
num1 = int(input("enter num1:--"))
num2 = int(input("enter num2:--"))

# Perform bitwise AND operation on two num1 and num2
print("Bitwise AND: ", num1 & num2)

# Perform bitwise OR operation on two num1 and num2
print("Bitwise OR: ", num1 | num2)

# Perform bitwise XOR operation on two num1 and num2
print("Bitwise XOR: ", num1 ^ num2)

# Perform bitwise NOT operation on two num1 and num2
print("Bitwise NOT of num1: ", ~num1)
print("Bitwise NOT of num2: ", ~num2)

# Perform Left Shift operation on two num1 and num2
print("num1 Left Shift by 2: ", num1 << 2)

# Perform Right Shift operation on two num1 and num2
print("num2 Right Shift by 2: ", num2 >> 2)
